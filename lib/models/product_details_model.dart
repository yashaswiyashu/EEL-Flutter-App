class ProductDetailsModel {
  final String uid;
  final String name;
  final String imageUrl;
  final String price;
  final String offers;
  final String description;
  final String numOfDays;

  ProductDetailsModel({
    required this.uid,
    required this.name, 
    required this.imageUrl,
    required this.price,
    required this.offers,
    required this.description,
    required this.numOfDays,
  });

}